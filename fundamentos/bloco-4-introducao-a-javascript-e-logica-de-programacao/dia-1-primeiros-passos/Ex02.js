// // Faça um programa que retorne o maior de dois números.
// Defina no começo do programa duas constantes com os valores
// que serão comparados.

const a = 20;
const b = 15;

if (a > b) {
	console.log(`${a} é o maior entre os números.`);
} else if (a < b) {
	console.log(`${b} é o maior entre os números.`);
} else {
	console.log("Ambos os números são iguais.");
}
